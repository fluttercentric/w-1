											<p>The William C. Vel√°squez Institute (WCVI) is a tax-exempt, non-profit, non-partisan public policy analysis organization chartered in 1985. WCVI (originally the Southwest Voter Research Institute) is a spinoff of the legacy organization Southwest Voter Registration Education Project (SVREP) founded by the late Latino icon Willie Velasquez. Willie correctly foresaw that Latino voters would inevitably overcome their political exclusion and needed to develop their policy and governance capacity.</p>

											<h3>The purpose of WCVI is:</h3>
											<ul>
												<li style="display: list-item; list-style-type: circle; margin-left: 30px;">To conduct research aimed at improving the level of political participation in Latino and other underrepresented communities;</li>
												<li style="display: list-item; list-style-type: circle; margin-left: 30px;">To provide policy strategies to Latino elected and civic leaders relevant to the needs of their constituents;</li>
												<li style="display: list-item; list-style-type: circle; margin-left: 30px;">To inform the Latino leadership and public about the impact of public policies on Latinos; and</li>
