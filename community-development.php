											<h2>Electric Vehicle Community Outreach Program</h2>

											<p>WCVI invites you to learn about Electric Vehicles. WCVI's Electric Vehicle Community Outreach Program is aimed at informing Latinos and minority communities about the benefits of Electric Vehicles (also known as EVs) and to promote LADWP’s programs and rebates!</p>

											<p>WCVI recognizes the systematic barriers communities of color face when it comes to access to EVs and charging stations. We aim to bridge that gap by engaging Los Angeles residents through Ride and Drive events, social media outreach and on the ground activities.</p>

											<p>Over summer 2022, a new rule was approved by the California Air Resources Board which will require all new cars sold in the state to be free of greenhouse gas emissions like carbon dioxide by the year 2035! With California’s requirement that all new cars sold be free of gas emissions by 2035 coupled with the fact that Latinos widely support efforts to mitigate climate change, WCVI feels this time is right to inform our community about the benefits of Electric Vehicles.</p>

											<p><b>FACTS</b>
											<br /><a href="https://www.ppic.org/blog/californians-see-air-pollution-as-a-problem-and-a-health-threat/">In California, 44% of Latinos live with poor air quality, compared to 25% of non-Latinos;</a>
											<br /><a href="https://www.pnas.org/doi/10.1073/pnas.1818859116">Black and Hispanic people experience a "pollution burden" of 56% and 63% more exposure than they cause, respectively.</a>
											<br /><a href="https://www.ucsusa.org/resources/ev-survey-equity#:~:text=Across%20all%20racial%20demographics%2C%20overall,Asian%20Americans%20say%20they%20would%20“">Latinos identified cost and access to a charging station as barriers to driving an EV;</a> and
											<br /><a href="https://www.ucsusa.org/resources/ev-survey-equity#read-online-content">43% of Latinos say they would "definitely" or "seriously consider" purchasing or leasing an EV as their next vehicle.</a></p>

