											<h2>Eco Intern Program 2023</h2>

											<p>WCVI's Eco Interns program provides an educational and mentorship program that inspires, empowers, and educates underrepresented minorities to pursue a career in the environment.</p>

											<p>For the 2023 Session, WCVI invites Los Angeles high school juniors and seniors with an interest in the environment to <a href="https://docs.google.com/forms/d/18vFjYJ1Z146PwsdczIsEiSio9LYh55B26ioA_dCWWwQ/viewform?edit_requested=true">apply here</a>.</p> 

											<p>Eco Interns will provide minority high school students the opportunity to learn about environmental studies and careers with outdoor activities and technological training. The program will provide a curriculum of environmental education and stewardship to prepare participants for environmental justice advocacy. Through the program, Eco Interns will learn skills that improve job opportunities and prepare them for higher education.</p>  

											<p>Eco Interns can participate in weekend sessions on environmental justice and environmental activities. Activities include: water testing, Los Angeles River history, tree planting, kayaking, as well as State Parks and local beach tours.</p>

