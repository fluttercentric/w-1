											<h2>Past Presidents of William C. Velasquez Institute</h2>

											<h3>William C. Velásquez: 1944-1988</h3>
											<img src="images/staff-Willie-Velasquez-photo.jpg" style="float:right; padding: 20px;">
											<p>William C. Velásquez was the founder of the Southwest Voter Registration Education Project in 1974 and Southwest Voter Research Institute in 1985. He envisioned a time when Latinos would play an important role in the American Democratic process. His legacy began in Texas.</p>

											<p>Velásquez was one of the founding members of the Mexican American Youth Organization (MAYO); a Chicano youth organization aimed at social action. His role in MAYO led to becoming Texas' first statewide Coordinator of El Movimiento Social de la Raza Unida, the precursor of La Ram Unida Party.</p>

											<p>His involvement with Latino organizations was extensive. In 1968 as Boycott Coordinator for the United Farm Workers (UFW), he organized strikes at the Rio Grande Valley of Texas. After leaving the UFW he became the founder and director of the Mexican American Unity Council in San Antonio, Texas. In 1970 he was named Field Director of the Southwest Council of La Raza.</p>

											<p>From 1972 to July 1974, he concentrated his efforts on building the Southwest Voter Registration Education Project (SVREP). Little notice was taken when Velasquez opened the doors to SVREP in 1974, seated on a folding chair; behind a small desk calling from a borrowed rotary telephone to spur Mexican Americans into politics.</p>

											<p>He enlisted the aid of Community organizers, together they launched hundreds of voter registration and get-the-vote-out (GOTV) campaigns throughout the southwest. The legacy of Velásquez is apparent-since its inception, SVREP has cultivated 50,000 community leaders, successfully litigated 85 voting rights lawsuits and has conducted 2,300 non-partisan,voter registration and GOTV campaigns. Consequently, voter registration has grown over the years from 2.4 million registered Latinos in 1974 to 7 million nationwide in 1998.</p>

											<p>But his vision involved more than just getting Latinos to the ballot box, Velásquez sought to bring into the democratic process an active and informed Latino electorate.</p>

											<p>Charted in 1985, the Southwest Voter Research Institute was established to seek the opinions of the Latino electorate and to make those findings known. He felt Latino leaders should be held accountable to their constituencies. As part of the institute, he set out to collect and distribute information on public policy issues ranging from income and poverty to U.S. and Latin America relations.</p>

											<p>In 1995 President Bill Clinton awarded Velásquez The Presidential Medal of Freedom, the highest honor any civilian can receive - and only the second Latino ever to earn that honor.</p>

											<p>In 1997, the Southwest Voter Research Institute was changed to the William C Velásquez Institute (WCVI) as a way to honor and perpetuate Velásquez' vision and legacy.</p>

											<p>But Velásquez did not get to see the fruits of his dreams, he passed away on June 15, 1988 of complications from kidney cancer. The ongoing activities of SVREP and WCVI serve as proof of the continued strength and merit of Velásquez.</p>

											<h3>Andy Hernandez</h3>
											<img src="images/staff-Andy-Hernandez_WCVI-site-history-.jpg" style="float:right; padding: 20px;">
											<p>Andrew "Andy" Hernandez took the helm as President, immediately after the untimely passing of the SVREP founder Willie C. Velásquez.</p>

											<p>Under Hernandez's leadership, SVRI developed its research department, redistricting efforts and Latin America Project (LAP). Later, the Southwest Voter Research Institute (SVRI), became the William C. Velásquez Institute (WCVI).</p>


											<h3>Antonio Gonzalez: 1956-2018</h3>
											<img src="images/staff-Antonio-Gonzalez-1.jpg" style="float:right; padding: 20px;">
											<p>As President of SVREP and WCVI, Antonio Gonzalez dedicated his life to empowering Latinos, from mobilizing voters and training novices and community leaders to become viable political candidates, to finding innovative solutions to the systemic barriers that our communities face. He also worked across borders building relationships with community members throughout the United States, Mexico, Central, and South America and Cuba. Antonio was a visionary and astute strategist whose impact will benefit communities for years to come.</p>
	
											<p>Antonio was named one of the 25 most influential Hispanics in America by Time Magazine in 2005 and was recently named by the Frederick Douglass Family Initiative as one of the "Frederick Douglass 200," a list of those who best embody the spirit and work of Frederick Douglass. The list also includes President Barack Obama, Congresswoman Nydia Velazquez, and Secretary Robert Reich, among others.</p>
 	
											<p>He was a pioneer in the area of environmental justice and was an early advocate for brown green policies in the Latino community, advocating for equitable and accessible open space.</p>
 		
											<p>A champion for the underserved and underrepresented, Antonio dedicated his life to fighting against all anti-immigrant, anti-Latino policies.</p>
 
											<p>Antonio's voice, legacy, and vision will live on in both SVREP's and WCVI's work to embolden the Latino community, give a voice to the disenfranchised, protect children, and to empower the oppressed.</p>
	
											<p>Antonio Gonzalez is survived by his wife Alma Martinez, daughters Sara and Isabel Gonzalez, his mother, brothers and sister, and family.</p>

