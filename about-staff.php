											<h3 style="margin:0; padding:0;">Lydia Camarillo, President</h3>
											<p>lcamarillo@wcvi.org</p>
											<img src="images/staff-LydiaCamarillo_Headshot.jpg" style="float:right; padding: 20px;">
											
											<p>Lydia Camarillo serves as President of the SVREP and WCVI, as well as serves on the SVREP and WCVI Board of Directors. Under the leadership of Lydia and SVREP’s former President González, Latino participation in the democratic process has tripled, from 5.4 million Latinos registered to vote in 1994 to 18.6 million in 2020. Latino voter turnout increased from 5 million to 16.6 million, from 1994 to 2020.</p>

											<p>In 1999, President Clinton and Vice President Gore appointed Lydia to serve as Chief Executive Officer (CEO) of the 2000 Democratic National Convention. At the convention, the presumptive nominee garnered the third-largest political bounce at 24 points in the last 70 years. Lydia was responsible for a $55 million budget, a staff of 350 members, a team of 1,000 production professionals, and more than 10,000 volunteers. She also worked on the Gore Campaign as an official surrogate after her assignment as CEO of the Convention.</p>

											<p>Lydia served as political and business consultant and served on the 2001 Villaraigosa Campaign. Lydia served from 1994 to 1999 as Executive Director of SVREP. Before joining SVREP, she served as National Leadership Director for the Mexican American Legal Defense and Educational Fund (MALDEF) from 1989 to 1994. She worked for the Latino Issues Forum, Monterey Bay Girl Scouts as field organizer before she joined MALDEF.</p>

											<p>Lydia serves on the Earth Day Network and the King Awards. She serves as Chair of the Texas Latino Redistricting Task Force, since 2011. The Taskforce composition was made up of 8 organizations and sued Texas on the 2011 Texas state redistricting congressional and state house maps. For the 2021 redistricting efforts, an additional 25 organizations have joined the Task Force. Lydia serves as Chair of the Texas Senate Latino Caucus and Mexican American Legislative Task Force.</p>

											<p>Lydia has served on numerous boards and commissions including the National Board of the Mexican American Legal Defense and Educational Fund (MALDEF) as 2nd Vice Chair, Personnel and Nominations Committee (2008 to 2014) and MPMC (MALDEF Building) Board of Directors (2014-2020). Texas ACLU Board of Directors, State Farm Advisory Committee, California Prop 40 Committee; member of the Executive Committee, Girls Scouts of America (1996-1999), San Antonio Planned Parenthood Board (1997-1999), Los Angeles Based City Project, an environmental justice law firm (2001-2012; she served as board chair from 2002 to 2008), Monterey County United Way Allocations Committee, Immigrant Rights Coalition for the Central Coast, Santa Cruz County Affirmative Action Commission, Salinas Affirmative Action Committee and the CA Task Force on Hispanics and the Civil Service.</p>

											<p>The "Movers and Shakers" of Elections and Campaigns Magazine, Working Women Magazine, People en Español, Latina Magazine, Latina Style Magazine, the California Journal, Hispanic Magazine, Hispanic Business Magazine, The Los Angeles Times has featured Lydia. She has been interviewed by hundreds of media organizations in the US and abroad.</p>

											<p>The Mexican American Legal Defense and Educational Fund (MALDEF) bestowed Lydia with the Lifetime Achievement -Excellence in Community Service Award at the San Antonio Gala on December 17, 2021. The National Organization for Women (NOW) named Lydia as one of the 100 Sisters in Suffrage in celebration of the 100th Anniversary of the 19th Amendment of the US Constitution. Among the women named are Ambassador Vilma Martinez, Antonia Hernandez, Stacey Abrams, Alice Paul, and Susan B. Anthony. The San Antonio Express News lists Lydia as one of the San Antonio Latino/a heroes and named SVREP founder Willie Velásquez as the number one on this list in 2016. On February 22, 2004, The Dallas Morning News included Lydia in its Distinguished Leaders series feature. Lydia has been named one of the 10 most influential Latinas in the 2000 September issue of Latina Magazine. Twice named one of the "100 Influential" Latinos in the United States in Hispanic Business Magazine -- in 2000 and in 1996, Lydia has received recognition and awards from other Latino and nonprofit organizations. Lydia and her husband Michael A. Cohen have two sons: Miguel A. Camarillo-Cohen and Antonio A. Camarillo-Cohen.</p>



											<h3 style="margin:0; padding:0;">Patricia Gonzales, Vice President</h3>
											<p>pgonzales@wcvi.org</p>

											<p>Patricia Gonzales is Vice President of the William C. Velasquez Institute (WCVI).</p>

											<p>Patricia joined WCVI in 2006. Previously, she worked with Southwest Voter Registration Education Project, a non-partisan voter mobilization entity for 9 years, first in field administration then as Chief of Staff and National Director of Development. She was recently named SVREP’s Director of Operations.</p>

											<p>Through her leadership, WCVI has expanded advocacy efforts in Texas. Through her efforts, WCVI has held the National Latino Congreso in Austin, Dallas and El Paso. She works to advance WCVI's mission through coalition building around environmental justice issues, immigrant justice, and the failed war on drugs.</p>

											<p>Patricia has been recognized by the San Antonio Business Journal as a rising "40 Under 40" star.</p>

											<p> graduate of the University of the Incarnate Word, Patricia holds her Bachelor's Degree in Business Administration with a concentration in Banking and Finance.</p>

