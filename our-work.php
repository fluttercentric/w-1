											<h2>WCVI's Programs</h2>

											<h3>Community Development</h3>
											<p>Since 1997 WCVI has programmed around the nexus of environment/climate change and Latino community development needs. Most relevantly since 2005 WCVI has programmed a climate adaptation water agenda with scores of Latino leaders and organizations leading to six national and state Latino environmental summits and conferences in Los Angeles, Dallas, El Paso, Austin and Chicago.</p>

											<ul style="list-style-type: circle; padding: 6px;">
												<li style="margin-left: 30px; margin-bottom: 12px;"><b>ProAgua:</b> A Climate Adaptation Leadership Initiative: Since 2004 in Los Angeles this program has resulted in training thousands of youth and leaders and successfully advocated for progressive water policies and barrio parks along the LA River (Marsh Park, Los Angeles State Historic Park, Rio Los Angeles Park) and a Stormwater Capture ordinance in Los Angeles County.</li>

												<li style="margin-left: 30px; margin-bottom: 12px;"><b>Eco Interns:</b> An educational program aimed at mentoring Los Angeles high school juniors and seniors with an interest in the environment. This hands-on internship program includes outdoor field activities and technological training focusing on areas along the Los Angeles River and conserving marine environments through experiential learning.</li>
												
												<li style="margin-left: 30px; margin-bottom: 12px;"><b>Energy Efficiency and Water Conservation Education:</b> outreach program to reach Latino communities about programs, rebates, and funds available to them.</li>
											</ul>	


											<h3>InterMestic Initiatives:</h3>
											<p>IMI has programmed since 1987 to educate US Latino leaders on key issues of US Latino America relations. Activities have included US policy in Central America, US-Cuba Relations; US-Mexico, US Venezuela, and ending the War on Drugs.</p>
											<ul style="list-style-type: circle; padding: 6px;">
												<li style="margin-left: 30px; margin-bottom: 12px;"><b>Latinos and US Latin American Relations:</b> Cuba, Mexico, and Venezuela: WCVI has conducted leadership exchanges to Cuba and Mexico, as well as educational campaigns promoting continuing engagement with Cuba and critiquing the current US policies (travel restrictions, border wall, sanctions) towards Cuba, Mexico and Venezuela.</li>

												<li style="margin-left: 30px; margin-bottom: 12px;"><b>Ending the War on Drugs:</b> WCVI successfully advocated for marijuana legalization in CA and continues to work in coalition to promote policy reform with an emphasis on drug decriminalization. Its efforts include advocating for medical freedom in Texas and developing progressive strategies for recreational legalization.</li>
											</ul>

